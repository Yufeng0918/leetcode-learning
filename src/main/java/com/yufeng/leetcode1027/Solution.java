    public int longestArithSeqLength(int[] A) {

        int len = A.length;
        int[][] dp = new int[len][200001];

        int ans = 2, diff, diffIdx;
        for(int i = 0; i < A.length; i++) {
            for(int j = 0; j < i; j++) {
                diff = A[i] - A[j];
                if (diff == 0) continue;
                diffIdx = diff + 10000;
                dp[i][diffIdx] = dp[j][diffIdx] + 1;
                ans = Math.max(dp[i][diffIdx], ans);
            }
        }
        return ans;
    }


